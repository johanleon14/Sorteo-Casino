    public void Iniciar() {

        if (i.getTam() != 0) {
            Nodo aux = i.getCabeza();
            while (aux != null) {
                if (numMaquina.getText().equals("")) {

                    JOptionPane.showMessageDialog(null, "No Ingresó Ninguna Máquina", "Aviso", 2);
                } else {
                    DefaultTableModel temp = (DefaultTableModel) tabla.getModel();

                    Object nuevo[] = {"", ""};
                    temp.addRow(nuevo);
                    tabla.setValueAt(numMaquina.getText(), contador, 0);

                    DefaultTableModel temp1 = (DefaultTableModel) tabla1.getModel();

                    Object nuevo1[] = {"", ""};
                    temp1.addRow(nuevo1);
                    tabla1.setValueAt(numMaquina.getText(), contador, 0);

                    i.agregarAlFinal(numMaquina.getText());

                    contador++;
                    numMaquina.setText("");
                }
                aux = aux.getSgte();
            }
        }
    }

