    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (numMaquinaEliminar.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "No se ha ingresadó ninguna máquina", "Aviso", 2);

        } else {
            i.elimRef(numMaquinaEliminar.getText());
            DefaultTableModel temp1 = (DefaultTableModel) tabla1.getModel();
            DefaultTableModel temp = (DefaultTableModel) tabla.getModel();
            temp1.removeRow(this.rowSel);
            temp.removeRow(this.rowSel);
            numMaquinaEliminar.setText("");
            JOptionPane.showMessageDialog(null, "La Máquina se ha Borrado", "Aviso", 1);
            btnEliminar.setEnabled(false);

            contador--;
        }


    }//GEN-LAST:event_btnEliminarActionPerformed

    private void numMaquinaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_numMaquinaKeyTyped
        int num = 4;
        char c = evt.getKeyChar();
        if (Character.isLetter(c)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(this, "Por favor solo ingrese números");
            numMaquina.setCursor(null);
        } else if ((int) evt.getKeyChar() > 32 && (int) evt.getKeyChar() <= 47
                || (int) evt.getKeyChar() >= 58 && (int) evt.getKeyChar() <= 64
                || (int) evt.getKeyChar() >= 91 && (int) evt.getKeyChar() <= 96
                || (int) evt.getKeyChar() >= 123 && (int) evt.getKeyChar() <= 255) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(this, "Por favor solo ingrese numeros");
            numMaquina.setCursor(null);
        }
        if (numMaquina.getText().length() >= num) {
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Por favor introduzca un código válido");
        }

    }//GEN-LAST:event_numMaquinaKeyTyped

