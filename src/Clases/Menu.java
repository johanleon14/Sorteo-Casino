    private void IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarActionPerformed

        if (numMaquina.getText().length() < 4) {
            numMaquina.setText("");
            JOptionPane.showMessageDialog(null, "No Ingresó un Código válido", "Aviso", 2);

        } else if (numMaquina.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "No Ingresó Ninguna Máquina", "Aviso", 2);
        } else if (i.Buscar(numMaquina.getText())) {

            numMaquina.setText("");
            JOptionPane.showMessageDialog(null, "El Código que ingresó ya existe", "Aviso", 2);

        } else {

            DefaultTableModel temp = (DefaultTableModel) tabla.getModel();

            Object nuevo[] = {"", ""};
            temp.addRow(nuevo);
            tabla.setValueAt(numMaquina.getText(), contador, 0);

            DefaultTableModel temp1 = (DefaultTableModel) tabla1.getModel();

            Object nuevo1[] = {"", ""};
            temp1.addRow(nuevo1);
            tabla1.setValueAt(numMaquina.getText(), contador, 0);

            i.agregarAlFinal(numMaquina.getText());
            contador++;
            numMaquina.setText("");

        }


    }//GEN-LAST:event_IngresarActionPerformed

